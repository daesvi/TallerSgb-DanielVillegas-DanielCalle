package model;

public class User {
    // Attribute
    private String nameUser;

    // Constructor
    public User(String nameUser) {
        this.nameUser = nameUser;
    }

    // Getters and Setters
    public String getNameUser() {
        return nameUser;
    }

    public void setNameUser(String nameUser) {
        this.nameUser = nameUser;
    }
}
